export const reverseThemeKey = (key) => {
  const isReverse = key.includes("reverse");
  const themeKey = key.replace("_reverse", "");

  return isReverse ? themeKey : `${key}_reverse`;
};

export const getThemeKey = (key) => {
  return key.replace("_reverse", "");
};

export const isReverse = (key) => {
  return key.includes("_reverse");
};